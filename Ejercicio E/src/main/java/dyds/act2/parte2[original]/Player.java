package dyds.act2.parte2;

public class Player {
	
	private String name; // song name
	private int born; // birth year

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getBorn() {
		return born;
	}
	public void setBorn(int born) {
		this.born = born;
	}
}
